import math # this module hold all mathematical function 
import math as m # this is just alias for math module

x = math.sqrt(25) # return squre root of a number
print(x)

x = math.pow(4,3) # return result of 4^3
print(x)

x = math.floor(4.99) # return floor value of 4.99 which is 4
print(x)

x = math.ceil(4.1) # return ceil value of 4.1 which is 5
print(x)

PI = math.pi # return constant value of PI 
print(PI)

E = math.e # return constant value of elif
print(E)

x = m.sqrt(13) # m is alias for math module
print(x)